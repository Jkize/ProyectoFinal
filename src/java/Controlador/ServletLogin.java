    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        DAO_Empleado empleado = new DAO_Empleado();
        String usua = request.getParameter("usuario");
        String cont = request.getParameter("contrasena");

        if (request.getParameter("Ingresar") != null) {
            try {
                int a = -1;
                if (usua != null && cont != null) {
                    a = empleado.validarUsuario((usua + "                                   ").substring(0, 35),
                            (cont + "               ").substring(0, 15));

                }
                if(a==1){
                    response.sendRedirect("ServletEmpleados?inicio=1");
                }
                
                
            } catch (ParseException ex) {
            }
        }
    }

